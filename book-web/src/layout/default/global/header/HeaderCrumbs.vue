<template>
  <div class="crumbs-content">
    <router-link
      v-for="item in getTagList"
      :key="item.path"
      :to="item.path"
      custom
      v-slot="{ isActive }"
    >
      <el-tag
        class="tag-item"
        :closable="getTagList.length > 1"
        :disable-transitions="false"
        @click="herdercrumbsClick(item.path)"
        :effect="isActive ? 'dark' : 'plain'"
        @close="closeTag(item.path)"
      >
        {{ item.meta.title }}
      </el-tag>
    </router-link>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
export default {
  name: 'HeaderCrumbs',
  computed: {
    ...mapGetters(['getTagList'])
  },
  methods: {
    herdercrumbsClick(item) {
      this.$router.push({ path: item })
    },
    closeTag(path) {
      this.$store.commit('menu/REMOVE_TAG', path)
    }
  }
}
</script>

<style scoped>
.crumbs-content {
  width: 100%;
  height: 40px;
  background: rgb(240, 240, 123);
  display: flex;
  align-items: center;
}

.crumbs-content .el-tag {
  margin-left: 5px;
  cursor: pointer;
}
</style>